                                        <h2>A Message from the Editor-In-Chief</h2>
                                    </div>
                                    <!-- entity_content -->
                                    
                                    <div class="entity_content">
                                        <p>Dutch philosopher Baruch Spinoza stated, “The highest activity a human being can attain is learning for
                                            understanding, because to understand is to be free.”</p>
                                        
                                        <p>This quote appears to be perfectly describing the problems I, along with many of my fellow classmates and friends,
                                            have been experiencing on college campuses across the country.</p>
                                        
                                        <p>Group based ideologies that limit freedom of thought and degrade public discourse by irrationally inserting fear
                                            within opposing sides have destroyed the intellectual playground that college is intended to be. Instead of allowing
                                            students to generate opinions on their own, professors across the country are inserting their own political beliefs
                                            into course materials in a way that forces students to disband their intuitions and align with what they are told.
                                            Furthermore, the growing partisan divide in the country has limited the opportunity for students to feel
                                            individualistic in their beliefs. Without the proper forum to express them, young adults are being forced to
                                            identify with two camps, or not express their beliefs at all. Students are not “learning to understand,” but rather
                                            learning subjective information provided to them. Spinoza would hardly consider us free.</p>
                                        
                                        <p>This is the purpose of the Bruin Political Review: an opportunity for students to struggle internally with their own
                                            personal beliefs on issues of their choice, and ultimately come to reasoned, researched, and well supported
                                            conclusions about them. The writers have the free will to express their own opinions, but are given the chance to do
                                            so in a thorough and thoughtful manner. This is not the place for catchy headlines that simplify nuanced issues and
                                            destroy any chance at meaningful debate about complex problems within American society. This is a place for real
                                            conversations to be generated, and true political discourse to be developed.</p>
                                        
                                        <p>In The Social Contract, Jean Jacques Rousseau wrote that “Man is born free, and everywhere he is in chains.” Today,
                                            this idea remains true. The chains are hypothetical, binding people only by limiting their ability to think for
                                            themselves and reflect upon the world around them. People are prevented from synthesizing their life experiences to
                                            come to purposeful conclusions about the society they live in, ultimately leading to more partisanship and less
                                            progress.</p>
                                        
                                        <p>Before I finish, I will leave you with these words:</p>
                                        
                                        <p>“If men are to be precluded from offering their sentiments on a matter, which may involve the most serious and alarming
                                        consequences that can invite the consideration of mankind, reason is of no use to us; the freedom of speech may be taken
                                        away, and dumb and silent we may be led, like sheep, to the slaughter.”
                                        -George Washington, first U.S. president</p>
                                        
                                        <p>Hopefully, the Bruin Political Review can make a difference, small as it may be, in promoting a meaningful political
                                            dialogue that is at the core of our country’s values of liberty, freedom, and justice.</p>
                                        
                                        <p>Sincerely,<br>
                                            Duke Fishman<br>
                                            Editor in Chief, Bruin Political Review</p>

