                                        <h1>Political Expression Should Be Covered in Anti-Discrimination Law</h1>
                                    </div>
                                    <!-- entity_title -->
                                    <div class="entity_meta">
                                        Dominic Cappello, Sept 24, 2020
                                    </div>
