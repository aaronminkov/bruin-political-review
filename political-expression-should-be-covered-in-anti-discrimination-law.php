                                    <div class="entity_content">
                                        <p>
                                            Political polarization- the gap between conservatives and liberals, has surged in recent years. According to Pew
                                            Research Center, the percentage of Americans who express consistently liberal or conservative views has doubled since
                                            1994, from 10% to 21%. Essentially, more Americans have moved to the right or left side of the political spectrum, with
                                            fewer Americans holding shared, overlapping beliefs. The percentage of Americans holding “mixed” ideological beliefs
                                            dropped from 49% in 2004, to 39% in 2014 [1].
                                        </p>
                                        <p>
                                            Furthermore, party animosity has also seen a rise during the same period. In 2004, 29% of Democrats saw the Republican
                                            party as a “very unfavorable” choice. In 2014, 38% of Democrats viewed Republicans as “very unfavorable”. Similarly, the
                                            percentage of Republicans who view the Democratic Party as “very unfavorable” jumped from 21% to 43% between 2004 and
                                            2014 [1].
                                        </p>
                                        <p>
                                            Even since Pew’s research done in 2014, political polarization has continued to rise. The 2016 Presidential election was
                                            a particularly hostile one, between Hillary Clinton and Donald Trump, both extremely polarizing figures to the opposing
                                            sides. Today, as the 2020 Presidential Election approaches, the political climate is once again heating up. Many factors
                                            have contributed to these current political tensions, including: the COVID-19 pandemic, economic turmoil, the resurgence
                                            of the Black Lives Matter (BLM) movement in response to the murder of George Floyd, the combination of widespread
                                            peaceful protests, as well as extremely violent ones, and the death of Supreme Court Justice Ruth Bader Ginsburg. These
                                            current events, in addition to the already increasing political polarization of the last decade, has created a harsh
                                            political climate, where holding certain political views has the capability to destroy relationships between
                                            individuals, and in some instances, careers.
                                        </p>
                                        <p>
                                            Currently, it is lawful for employers to fire an employee, based on their political beliefs, political speech, or
                                            political association. With an increasingly polarized climate, employees being terminated for these reasons has been
                                            frequent.
                                        </p>
                                        <p>
                                            Many believe that the 1st Amendment of the U.S. The Constitution protects employees in these instances. The 1st
                                            Amendment gives citizens the right to freedom of speech, freedom of religion, freedom of press, and the right to
                                            peacefully assemble [2]. This amendment only protects citizens from the government infringing upon these rights. It does
                                            not apply to the private sector. An individual employed by the U.S. or state governments, can not be fired based on
                                            their political beliefs, speech, or association, as it is protected under the 1st amendment. Individuals employed in the
                                            private sector, however, do not share these same protections. It is 100% lawful for a private employer to fire an
                                            employee based on the employee’s speech or political beliefs.
                                        </p>
                                        <p>
                                            In Maryland, Arthur Love, previously the Deputy Director of the Governor’s Office of Community Initiatives, was fired
                                            for posting “divisive” statements, images, and memes on social media [3]3. The Baltimore Sun reported that Love’s posts
                                            were supporting Kyle Rittenhouse, the 17-year-old who killed two protestors following the murder of Jacob Blake in
                                            Kenosha, Wisconsin. Love called one of the shooting victims a “deceased terrorist”, and said “don’t be a thug if you
                                            can’t take a lug.” Furthermore, Love expressed his ultra-conservative views by denouncing “pedophelia BS” from “the
                                            left” in response to a bisexual cartoon character. Love is now pursuing legal action. Although his statements could be
                                            viewed as inappropriate and insensitive, his termination from public office could be deemed unlawful. In this case, Love
                                            could be protected by the 1st amendment of the U.S. Constitution.
                                        </p>
                                        <p>
                                            In another case, Dave Sunderland, an 55-year old employee of Newport News Shipbuilding in New Jersey, was fired this
                                            year for wearing a “Trump 2020” baseball cap to work. Sunderland told the Daily Press that he has been wearing Donald
                                            Trump hats to work for the last 4 years, and that it wasn’t until 2020 that it became an issue that resulted in his
                                            termination [4]. Spokesperson for Newport Duane Bourne stated, “we do not allow political campaign or partisan political
                                            activities on company property, such as wearing attire with messages that include a campaign slogan”... and “political
                                            messages, debates and commentaries on candidates and related issues should not take place on company time and interfere
                                            with normal business operations”. Sunderland responded that he wasn’t campaigning, he was simply wearing a cap, an
                                            action that did not interfere with the workplace operations. Sunderland also noted that throughout his employment at
                                            Newport, he had witnessed other employees wearing Barack Obama attire in 2012, Hillary Clinton T-shirts in 2016, and
                                            Black Lives Matter masks in 2020.
                                        </p>
                                        <p>
                                            In this instance however, Newport News Shipbuilding is a private entity. It is owned by Huntington Ingalls Industries, a
                                            private corporation that is contracted by the U.S. government for various manufacturing purposes. Because Newport News
                                            Shipbuilding is in the private sector, Sunderland is not protected by the 1st Amendment of the Constitution, which would
                                            give him freedom of expression. The private corporation has the ability to create their own policies, including firing
                                            employees based on the employee’s political expression and affiliation. Sunderland has no ability to sue his employer
                                            for wrongful termination.
                                        </p>
                                        <p>
                                            There have been numerous examples of these terminations based on political speech, inflammatory speech, and political
                                            expression in the last year. A teacher in Harford, Maryland was fired for posting a picture of herself giving the middle
                                            finger to a Trump/Pence bumper sticker [5]. This specific example displays that individuals are not only being fired for
                                            conservative/ right-wing affiliation, as this teacher was fired for expressing an anti-Trump view. A police captain in
                                            Greenwich, Connecticut was fired for purchasing and displaying yard signs in support of Donald Trump and a local
                                            Republican running for office [6]. A station manager at ASU’s student run Blaze Radio was fired for a tweet she posted
                                            about Jacob Blake [7]. And a USC professor of business communication was fired for using a Chinese word that “sounded
                                            similar” to the N-word [8].
                                        </p>
                                        <p>
                                            As mentioned earlier, in the private sector, employers can fire/hire employees at their own discretion. However, to
                                            protect employees from discrimination in the private sector, there are federal anti-discrimination laws. These laws are
                                            enforced by the U.S. Equal Employment Opportunity Commission (EEOC). One of the major anti-discrimination laws is Title
                                            VII of the 1964 Civil Rights Act, which “makes it illegal to discriminate against someone on the basis of race, color,
                                            religion, national origin, or sex” [9]. Furthermore, the Pregnancy Discrimination Act was added to Title VII to prevent
                                            discrimination based on pregnancy and childbirth. Additional anti-discrimination laws include: the Age Discrimination in
                                            Employment Act of 1967, preventing discrimination to individuals over the age of 40, Title I of the Americans with
                                            Disabilities Act, preventing discrimination to qualified individuals with disabilities, and the Genetic Non
                                            Discrimination Act of 2008, preventing discrimination based on genetic information, such as a family history of a
                                            certain disease or condition [9].
                                        </p>
                                        <p>
                                            If an individual is fired for a reason that violates one of the above laws, that individual has the ability to sue the
                                            employer for wrongful termination. In the case of a wrongful termination lawsuit, it is up to a judge or jury to decide
                                            if the employee was unlawfully terminated. Per Cornell Law School, “the plaintiff must show unlawful action such as
                                            illegal discrimination” [10].
                                        </p>
                                        <p>
                                            Anti-discrimination laws protect employees from wrongful termination, however there is no anti-discrimination laws
                                            regarding political speech, political expression, or political association and affiliation. One might argue that
                                            anti-discrimination laws protect individuals from discrimination based on factors they can’t control, such as race, age,
                                            genetic history, etc. In response, religion, a controllable factor, is covered in the Civil Rights Act. What makes
                                            religion different from political affiliation? A Muslim woman can’t be fired for wearing a hijab to work, and a Jewish
                                            man can’t be fired for wearing a yarmulke. How is that different from expressing support for the Democratic Party, the
                                            Republican Party, Joe Biden, Donald Trump, Bernie Sanders, the Black Lives Matter Movement, the LGBTQ movement, etc? If
                                            the employer argues that such political expression interferes with the workplace, that should be left to the decision of
                                            a judge or jury. Employees should have the right to sue their employer for wrongful termination if they are fired for
                                            political expression or affiliation. These employees deserve the ability to at least get an appearance in court. Then,
                                            the judge or jury can make the final decision on if the termination was lawful or unlawful.
                                        <p>

                                        </p>
